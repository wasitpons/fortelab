// Language switching functionality
let currentLang = 'en';

function switchLanguage(lang) {
  if (lang === currentLang) return;
  
  currentLang = lang;
  updateContent(lang);
  updateLanguageSwitcher(lang);
  
  // Store language preference
  localStorage.setItem('fortelab_language', lang);
}

function updateContent(lang) {
  const t = translations[lang];
  
  // Generic function to update all elements with data-i18n attributes
  const updateElement = (selector, text) => {
    const elements = document.querySelectorAll(selector);
    elements.forEach(el => {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = text;
      } else {
        el.textContent = text;
      }
    });
  };
  
  // Update navigation
  updateElement('[data-i18n="nav.solutions"]', t.nav.solutions);
  updateElement('[data-i18n="nav.useCases"]', t.nav.useCases);
  updateElement('[data-i18n="nav.newsletter"]', t.nav.newsletter);
  updateElement('[data-i18n="nav.why"]', t.nav.why);
  updateElement('[data-i18n="nav.developers"]', t.nav.developers);
  updateElement('[data-i18n="nav.contact"]', t.nav.contact);
  
  // Update hero section
  updateElement('[data-i18n="hero.title"]', t.hero.title);
  updateElement('[data-i18n="hero.subtitle"]', t.hero.subtitle);
  updateElement('[data-i18n="hero.cta1"]', t.hero.cta1);
  updateElement('[data-i18n="hero.cta2"]', t.hero.cta2);
  updateElement('[data-i18n="hero.trustedBy"]', t.hero.trustedBy);
  updateElement('[data-i18n="hero.bullet1"]', t.hero.bullets[0]);
  updateElement('[data-i18n="hero.bullet2"]', t.hero.bullets[1]);
  updateElement('[data-i18n="hero.bullet3"]', t.hero.bullets[2]);
  
  // Update solutions section
  updateElement('[data-i18n="solutions.title"]', t.solutions.title);
  updateElement('[data-i18n="solutions.subtitle"]', t.solutions.subtitle);
  updateElement('[data-i18n="solutions.card1.title"]', t.solutions.cards[0].title);
  updateElement('[data-i18n="solutions.card1.description"]', t.solutions.cards[0].description);
  updateElement('[data-i18n="solutions.card1.cta"]', t.solutions.cards[0].cta);
  updateElement('[data-i18n="solutions.card2.title"]', t.solutions.cards[1].title);
  updateElement('[data-i18n="solutions.card2.description"]', t.solutions.cards[1].description);
  updateElement('[data-i18n="solutions.card2.cta"]', t.solutions.cards[1].cta);
  updateElement('[data-i18n="solutions.card3.title"]', t.solutions.cards[2].title);
  updateElement('[data-i18n="solutions.card3.description"]', t.solutions.cards[2].description);
  updateElement('[data-i18n="solutions.card3.cta"]', t.solutions.cards[2].cta);
  
  // Update other sections
  updateUseCases(t.usecases);
  updateNewsletter(t.newsletter);
  updateWhy(t.why);
  updateDevelopers(t.developers);
  updateSecurity(t.security);
  updateContact(t.contact);
  updateFooter(t.footer);
  
  // Update document language attribute
  document.documentElement.lang = lang;
  
  // Update body class for CSS styling
  document.body.className = document.body.className.replace(/lang-\w+/, '') + ` lang-${lang}`;
}

