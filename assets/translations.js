// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  // Get saved language preference or default to English
  const savedLang = localStorage.getItem('fortelab_language') || 'en';
  currentLang = savedLang;
  
  // Update content and switcher
  updateContent(savedLang);
  updateLanguageSwitcher(savedLang);
  
  // Add event listeners to language switcher buttons
  document.querySelectorAll('[data-lang-switch]').forEach(button => {
    button.addEventListener('click', function(e) {
      e.preventDefault();
      switchLanguage(this.dataset.langSwitch);
    });
  });
});